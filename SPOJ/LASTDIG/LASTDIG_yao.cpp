#include<cstdio>

int _,
	t[10][4] = {
	{0,0,0,0},
	{1,1,1,1},
	{6,2,4,8},
	{1,3,9,7},
	{6,4,6,4},
	{5,5,5,5},
	{6,6,6,6},
	{1,7,9,3},
	{6,8,4,2},
	{1,9,1,9}
	};

int main()
{
	int a, b;
	for (scanf("%d", &_); _; --_)
	{
		scanf("%d%d", &a, &b);
		if (b==0)
			printf("1\n");
		else
			printf("%d\n", t[a%10][b%4]);
		_--;
	}
}
